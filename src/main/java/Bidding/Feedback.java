package Bidding;

public class Feedback {
	private int auction_id;
	private String sellerid;
	private int seller_rate;
	/**
	 * @return the auction_id
	 */
	public int getAuction_id() {
		return auction_id;
	}
	/**
	 * @param auction_id the auction_id to set
	 */
	public void setAuction_id(int auction_id) {
		this.auction_id = auction_id;
	}
	/**
	 * @return the sellerid
	 */
	public String getSellerid() {
		return sellerid;
	}
	/**
	 * @param sellerid the sellerid to set
	 */
	public void setSellerid(String sellerid) {
		this.sellerid = sellerid;
	}
	/**
	 * @return the seller_rate
	 */
	public int getSeller_rate() {
		return seller_rate;
	}
	/**
	 * @param seller_rate the seller_rate to set
	 */
	public void setSeller_rate(int seller_rate) {
		this.seller_rate = seller_rate;
	}
	
	
}
